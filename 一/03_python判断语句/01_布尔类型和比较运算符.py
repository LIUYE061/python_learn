"""
演示布尔类型的定义
以及比较运算符的应用
"""
# 定义变量存储布尔类型的数据
bool_1 = True
bool_2 = False
print(f"bool_1: {bool_1},类型是: {type(bool_1)}")
print(f"bool_1: {bool_2},类型是: {type(bool_2)}")

# 比较运算符的使用
# == , != , > , < , >= , <=
num1 = 10
num2 = 10
print(f"10 == 10 : {num1 == num2}")

num1 = "itcast"
num2 = "itheima"
print(f"itcast == itheima : {num1 == num2}")