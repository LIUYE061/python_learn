# 将数字类型转换为字符串
number_str = str(666)
print(type(number_str),number_str)

float_str = str(13.14)
print(type(float_str),float_str
      )
# 将字符串转换为数字
str_int = int("666")
print(type(str_int),str_int)

str_float = float("13.14")
print(type(str_float),str_float)

# 整数转浮点数
int_float = float(666)
print(type(int_float),int_float)

# 浮点数转整数(舍弃小数部分)
float_int = int(13.55)
print(type(float_int),float_int)